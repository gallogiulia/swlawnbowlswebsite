<h1>PLAYDOWNS</h1>


<p>Playdowns are tournaments that determine the men and women who represent their division at the <em>Bowls USA</em> National Championships. Our Playdowns include singles and pairs competitions that are open to all U.S. citizen <em>Bowls USA</em> members of the Southwest Division.</p>

<p>Men and women compete separately. The Playdowns occur over two weekends (see the calendar). The rules are contained in the Men's Blue Book (<a href="../../documents/rules_m/play_1.pdf">singles</a> - <a href="../../documents/rules_m/play_2.pdf">pairs</a>) and Women's <a href="documents/rules_w/play.pdf">Tournament Manual.</a></p>

<p>The other six <em>Bowls USA</em> divisions send one entry in each of the four categories to the Championships, but due to the population of our division and the need to have an even number of entries, the Southwest sends two (identified <em>Stars</em> and <em>Stripes</em>).</p>
<p>National Championship singles winners represent the U.S. in the World Singles Champion of Champions the following year.</p>

<p><a href="http://www.bowlsusa.us/national-championships.html">Visit this <em>Bowls USA</em> page</a> for details of the Championships.</p>
