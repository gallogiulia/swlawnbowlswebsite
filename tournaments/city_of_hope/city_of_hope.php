<p><strong>THE CITY OF HOPE WINS AGAIN IN LAWN BOWLING</strong></p>
<div class="photos"><img src="2011_pic_triples_01.png" width="300" height="201" alt="Winners"><br>
Ryan Neptune - Scott Roberts - Bob Beck<br>
<br>
<img src="2011_pic_triples_02.png" width="300" height="201" alt="Winners">Tom Davis - Eileen Lancendorfer - Joel Fruehan<br>
<br>

<img src="2011_pic_triples_03.png" width="300" height="214" alt="Check"><br>
Mrs.Gayle Plunkett presenting $3425.to Mrs.&nbsp;Brenda&nbsp;Warner from the Whittier Auxiliary of the City of Hope, "Together for Hope."
</div>
<p>The 41st City of Hope Mixed Triples Tournament was held on June 4th at the Santa Anita Bowling Green Club facilities in the Arcadia County Park. It was another beautiful day in Southern California, cool and breezy.<br>
<br>
This year there were 30 teams comprised of 90 lawn bowlers from around the area. From San Clemente, Riverside, Long Beach, Laguna Beach, Santa Barbara, Newport harbor, Friendly Valley, Pasadena, Alhambra, Pasadena, el Monte and South El Monte, Pomona, San Gabriel, Temple City and several other towns.</p>
<p> The ladies from the Whittier Auxiliary of the City of Hope, "Together for Hope" provided many types of cookies, bottled water and soda. They show up each year with the &quot;goodies&quot; for the bowlers. The auxiliary not only provides the food items, they also provide the triangular lapel pins stating the 41st games and the year 2011. They do this each year and the lawn bowlers repay them with all of the money collected from entry fees and donations. This year Mrs. Gayle Plunkett presented Mrs. Brenda Warner with an envelope containing $3425.00 in checks payable to the City of Hope. It cost each lawn bowler a minimum of $20.00 to enter the games, but many give much more. Gene and Gayle Plunkett provided the food for the luncheon. They have donated the food for the past several years. The Santa Anita Bowling Green Club also provides the three plaques for the winning team on each of the two greens.</p>
<p> The bowlers are divided into two greens. Green "A" are the more experienced bowlers and Green ³B² the up and coming bowlers. This year the winning team on the "A" Green was Scott Roberts, Ryan Neptune and Bob&nbsp;Beck from the Laguna Beach Lawn Bowling Club. The "B" Green winners were Tom&nbsp;Davis, Eileen Lancendorfer and Joel Fruehan of the Long Beach Lawn Bowling Club.</p>
<p> The Santa Anita club had entries trying to get those plaques. Fred&nbsp;Robles, Marinko Tudor, Margi Rambo, John Gonzales, Shuman and Amy&nbsp;Chan, Bob&nbsp;Hauerwaas, Dick Ashmore, Dewey Tse, Bob Schmalholz, Peter Iles, Ray&nbsp;Obazawa, Gene Plunkett, Steve and Jean Dowd, Jim and Mattie&nbsp;Miller, Kay&nbsp;Tong, Fan Hung, Ka Yu, Norm Wiles, Barry Hayes, Bob Ratcliffe, Irene&nbsp;Mastro, Juanita&nbsp;Jones, Sean&nbsp;McMorris, Mary&nbsp;Russell and Liam&nbsp;Courtney.</p>
<p> Those in the hospitality section were: Gayle Plunkett, Carol Bucey, Marian Hauerwaas, Betty&nbsp;Barnett, Betty&nbsp;Snelling and Amy&nbsp;Chan. It was a team effort and everyone pitched in to make this the best City of Hope Tournament in 41 years.<br>
<br>
</p>


