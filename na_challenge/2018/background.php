<h1>BACKGROUND</h1>

<p>In 1989, Lyall Adams of Canada and USA Secretary-Treasurer Ed Quo discussed the possibility of a series of Canada vs. USA International bowls events. Further correspondence between Ed and Canadian counterparts Gord Law, and then Margaret Fettes, led to the North American Challenge, first played in 1995 at Sun City, AZ.</p>

<p>The initial North American Challenge was comprised of teams of five men and five women, evolving later into two teams of ten men and ten women from each country. The ten-player teams are divided into two five-member squads designated Folkins and Jarvis, in honor of two great bowlers, Dick Folkins (USA) and Graham Jarvis (Canada).</p>

<p>The first NAC Procedures Manual was initiated by Adele Patterson (USA) for the 1998 NAC staged at her club at Santa Rosa, CA. Subsequently, John Stewart (USA) expanded and systematized the Manual, which was approved by Bowls Canada and the (then) USLBA. The NAC was played annually until 2008 when a biennial format was adopted at the suggestion of Laura Dewald (Canada). Games alternate between locations in the USA and Canada.</p>

<p>The first event in 1995 was co-chaired by Charlotte O'Keefe and Gil Stephan, who is now the USA NAC Chair, overseeing the event when staged in the USA. The players for Team USA are selected by the men's and women's National Team Selection Committees. For Canada the NAC is overseen by Bowls Canada Boulingrin's High Performance Program.</p>

