<p>We are a <em>Bowls USA</em> division that includes two independent organizations:</p>


<p class="desc_women_div"><strong class="text_women">

<!--
<img src="about_us/swwd_logo.png" alt="" width="120" height="120" class="floatRight"/>
-->

Southwest Women's Division (SWWD). </strong>The SWWD is governed by its Delegate Assembly (the Executive Board, Councilor and two women delegates from each of the Southwest Division's clubs). It is governed by its <a href="documents/SWWD_Constitution.pdf">Constitution and By-laws</a>. The Delegate Assembly meets three times each year and holds an Annual General Meeting in January. <a href="mailto:swwd.officers@gmail.com">Send&nbsp;e-mail.</a></p>

<!--
<img src="about_us/swlba_logo.png" alt="" width="120" height="120" class="floatRight"/>
-->

<p><span class="desc_men_div"><strong class="text_men">Southwest Lawn Bowls Association (SWLBA)</strong>. The (men's) SWLBA is  governed by an Executive Board consisting of its elected officers and district representatives (or alternates). It is governed by its <a href="documents/SWLBA_By-laws_2013_01_19.pdf">By-laws.</a> The Board typically meets three times each year. The SWLBA holds an Annual General Meeting in January</span> with three representatives from each club.</p>

