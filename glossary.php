<h1>Glossary</h1>

<p>The terms and acronyms defined immediately below relate specifically to the Southwest Division. General lawn bowling terms are defined in the <em>General Lawn Bowling Terms</em> (included below).</p>

<table id="glossary">
<tr>
<th>Blue Book</th>
<td>The tournament manual for the men's division (i.e., SWLBA). It prescribes the rules for SWLBA-sponsored tournaments (find it in the Documents page).</td>
</tr>
<tr>
<th>Bowls USA</th>
<td>The governing organization for bowls in the U.S.</td>
</tr>
<tr>
<th>District</th>
<td>One of seven portions of the Southwest Division as established by the SWLBA. Districts include clubs in a geographic area (note: the SWWD is not divided into districts).</td>
</tr>
<tr>
<th>Green Jack</th>
<td>The newsletter of the SWLBA - it was printed several times a year until 2013</td>
</tr>


<tr>


<th>Handbook</th>
<td><a name="handbook"></a>
An annual document prepared in the Spring that is distributed to clubs and division officers. It details member &amp; club information; a club directory; bowling schedules; current year schedule and prior year tournament results.</td>
</tr>
<tr>
<th>Home Club</th>
<td>For people who are members of more than one club, the club that pays the person's SWLBA or SWWD dues</td>
</tr>

