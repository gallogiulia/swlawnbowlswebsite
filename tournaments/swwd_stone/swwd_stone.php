<h3>About Katy Stone...</h3>

<p>Katy Stone, longtime Long Beach resident, passed away at home on March&nbsp;19,&nbsp;2010. Katy worked for many decades in the Long Beach Department of Parks, Recreation, and Marine, where her roles included superintendent of recreation services and superintendent of aquatics. Multiple generations of Long Beach youth enjoyed the programs that she created and managed.An avid sportswoman, Katy played volleyball and softball, fished, and worked as a ski instructor. After her retirement, she started running marathons and became a world-class lawn bowler. As a member of the Long Beach Lawn Bowling Club, she was on the U.S. national lawn bowling team from 1999 to 2008, and was the U.S. women's singles champion in 2000 and women's pairs champion in 2005. Beloved for her wicked sense of humor and vivid personality, Katy made friends everywhere she went. As a teacher, mentor, manager, sister, and aunt, Katy had a special gift for helping people realize their potential. She lived her philosophy: to meld work and play, to develop both body and mind, and always act in a generous, fun
loving spirit. She will be greatly missed.</p>
