<p class="font_12"><em>(the Guidelines and Rules are also provided below)</em></p>
<p><strong><u>Vision Statement:</u></strong></p>
<p>To provide an environment for the Division&rsquo;s club bowlers, of both genders, and of all levels of experience and ability, that combines competition with the opportunities to socialize with other club members. </p>
<p><strong><u>Teams: </u></strong></p>
<p>All clubs will be encouraged to join either league, and can enter as many teams as they believe they can support. </p>
<p><u>A roster for every team must be provided. All players that are on the roster may play any position for that team.  Players on the roster for one team may not play for any other team.  Players who are not on the roster for a team may play as a substitute, but substitutes are not allowed to skip or play singles.</u></p>
<p>Generally, 6-7 players are needed to support a 4-person team (illness or injury, vacations etc.). The league stresses this point. Because of the tight time frame of the league, games should only be rescheduled because of rain. <em>They cannot be rescheduled because of player unavailability. </em>Teams would need to ensure that they have sufficient back up players able to fill in as necessary. If clubs would prefer to play the match during the week or on Sunday it is alright if both clubs can agree to a change. Playing on Saturday is not mandatory except for the playoffs. </p>
<p>Substitutes who are not one of the named 4 persons on a team may come from a pool of players and may play on any team.  Silver team members can also substitute for a club&rsquo;s Gold team.</p>
<p>Substitutes can be made between the morning and afternoon games.  If the afternoon players are on the roster they can play any position.</p>
<p>A player who plays for one club cannot play for another club.</p>
<p>The wearing of matching colored uniforms for each team is encouraged. Wear whites if uniforms are not worn. </p>
<p>An engraved trophy will be awarded to the team winning the Playoffs of the Gold and Silver Winter League. The Gold League team that wins will get their name engraved on a perpetual trophy which they will keep until the next year. </p>
<p><strong><u>Structure: </u></strong><u></u><br>
 The competition will be in the form of regionalized leagues. The League will be sensitive to geographical and travel considerations. There will be a North and South Division with a Gold and Silver league in each Division. If possible the two leagues will meet in a playoff final but that is to be determined by the number of teams entered in each league and if the league stays on schedule. </p>
<p>There will be 2 tiers of competition: </p>
<p><strong><u>Gold League</u></strong></p>
<p>Gold League will be accented on the competitive aspect, and will involve the more experienced and accomplished bowlers within the Division. </p>
<p><strong><u>Format: </u></strong><u></u><br>
 Each team will consist of 4 players (men, women, or combined). There will be two games of pairs (15 ends) in the morning and game of Singles (15 ends) and Triples (15 ends) in the afternoon. Club 1 Pair A will play Club 2 Pair A and Club 1 Pair B will play Club 2 Pair B </p>
<p><strong><u>Scoring: </u></strong><u></u><br>
 Each match will consist of 15 ends divided into 3 sets of 5 ends. Each set will be worth 2 points. The team that scores the most points in that set will win the points for that specific set. Should the set be tied in the number of points scored each team will get 1 point. <br>
 The team that wins the match (total score after 15 ends) will earn 4 points. Should the match end in a tie both teams will score 2 points. <br>
 There will be a total of 10 points per match (6 set points and 4 match points) and a total of 40 points available for the day. </p>
<p><strong><u>Silver League</u></strong></p>
<p>Silver League will be focused on learning and social aspects of the game.  This league is composed of those bowlers who are newer to the game and would like to develop their game further. It is hoped that the more experienced bowlers would mentor the newer bowlers.  It is hoped that more competitive and experienced bowlers will be playing in the Gold League.  Bowlers who have finished in the top 3 in a SW points event in the last two years are not allowed to play in a Silver League game.  A separate list has been provided of all the players who are ineligible from playing in the Silver League.</p>

<p><strong>Format: </strong><br>
 Each team will consist of 4 players (men, women, or combined). There will be two games of pairs (15 ends) in the morning and game of Singles (15 ends) and Triples (15 ends) in the afternoon. Club 1 Pair A will play Club 2 Pair A and Club 1 Pair B will play Club 2 Pair B </p>
<p><strong>Scoring: </strong><br>
 Each match will consist of 15 ends divided into 3 sets of 5 ends. Each set will be worth 2 points. The team that scores the most points in that set will win the points for that specific set. Should the set be tied in the number of points scored each team will get 1 point. <br>
 The team that wins the match (total score after 15 ends) will earn 4 points. Should the match end in a tie both teams will score 2 points. <br>
 There will be a total of 10 points per match (6 set points and 4 match points) and a total of 40 points available for the day. </p>
<p><u><strong>Playoffs:</strong></u></p>
<p>At the end of the year the playoff seeding will be based on the teams that accrue the most points during the regular season. Wins and losses will not matter as far as playoff seeding. It will solely be based off points. In the event of a tie for playoff seeding the following tie breakers will be used: </p>
<p>1. Head to Head results vs the team you are tied with. </p>
<p>2. Your score vs the highest seeded playoff team. If that does not break the tie then keep going down all teams in order of seeding until the tie is broken. </p>
<p>For the Silver League 4 teams will make the playoffs for both the North and South. In each division the #1 seed would play the # 4 seed and the # 2 seed would play the #3. The higher seed would be the home team at any point during the playoffs. Ideally (based on # of teams entered) the playoffs will last 3 weeks where the 3rd week the winner of the North division will play against the winner of the South division, and the two division losers will play each other in the 3rd place game</p>
<p>For the Gold League, 8 teams will make the playoffs from the North, South 1 and South 2 Divisions.  The winner of each division will be ranked #1 - #3 based on points per game.&nbsp; The next top five teams from the three divisions will be ranked by points per game to get #4 - #8 teams.&nbsp; Points per game will be used as the deciding factor as the North will be playing four games, while the South will be playing six. &nbsp;#1 team will play at home against team #8. &nbsp;#2 team will play at home against team #7, and so on in the first round.&nbsp; After the first round is over, four teams will remain and they will be re-seeded so the top two teams will be at home against the lower-ranked two teams.&nbsp; The two winners will play in the final while the two losers will play in the 3rd place game. </p>
<p><strong>Cost: </strong><br>
 The cost would be $30 per team of 4, regardless of Gold or Silver level. This would cover any administrative expenses and the purchase of trophies. Checks would be made out to: <em>SWLBA</em>. </p>
<p><strong>Green Fees: </strong><br>
 Green fees should be paid to the home club upon arrival.</p>
<p><strong>Results/Administration: </strong><br>
 Please email or call in the results after completion of your weekly matches to the league chairperson. <br>
 For questions/issues relating to league structure, match days and time, or player eligibility, please call or email the league chairperson. </p>
<p><strong>RULES AND FORMAT OF PLAY: </strong><br>
 1. Games are scheduled to start at 9am on the scheduled date unless the two clubs agree to an alternate date. <br>
 <span class="indent_50">a. Complete teams should arrive by 8:30 am. </span><br>
 <span class="indent_50">b. Players should bring their own lunch. Hospitality is not expected from the host club. </span><br>
 <span class="indent_50">c. Captains will call the opponent&rsquo;s captain to confirm the match is on no later than</span><br>
  <span class="indent_70">Friday evening.</span><br>
 <span class="indent_50">d. If a full team cannot be fielded, the captain should call the opposing captain and </span><br>
 <span class="indent_70">the league. If amatch is forfeited the winning team will get full points awarded </span><br>
 <span class="indent_70">and the forfeiting team will get 0 points (TL +30/0 and DL +40/0). </span><br>
 <span class="indent_50">e. If a match is rained out, please re-schedule a makeup date with the opposing </span><br>
 <span class="indent_70">captain and let the league know about the change. If possible, make up the match a</span><br>
 <span class="indent_70">s quickly as possible so the league can remain on schedule. </span><br>
 <span class="indent_50">f. In all cases, call the host club if weather is questionable. It could be raining in your </span><br>
 <span class="indent_70">area and sunny at your opponents club. </span></p>
<p>2. The 1 minute mat rule will apply to the Gold League. </p>
<p>3. A maximum of two dead ends per game regardless of which team kills the end.  On the third dead end and after, the jack shall be respotted according to rule 15.5, which is 2 meters out from the ditch and, if it went out to the side, 1.5 meters toward the side it went out on.</p>
<p>4. In the event of any unforeseen circumstances the Winter League Chairperson will have final ruling as to any modifications of start times, format changes or rule interpretations. </p>
<p>5.  Late Arrivals will be handled as in the SWLBA Blue Book.  &ldquo;<em>Late arrivals will be penalized one (1) point for each ten (10) minutes from the end of trial ends, or fraction thereof, and will count as an end played. Thirty (30) minutes delay constitutes a forfeiture of the game. Such penalties may be waived by the Tournament Director due to extenuating circumstances</em>.&rdquo;</p>
<p><strong><u>Scoring Examples:</u></strong><br>
 Club A Pairs Team #1 wins 2 sets (4 points) and the match (4 points) for a total of 8 points.  Club B wins 1 set (2 points) for a total of 2 points. <br>
Club A Pairs Team #2 wins 2 sets (4 points) and the match (4 points) for a total of 8 points.  Club B wins 1 set (2 points) for a total of 2 points. <br>
Club A Singles wins 2 sets (4 points) and the match (4 points) for a total of 8 points.  Club B wins 1 set (2 points) for a total of 2 points. <br>
Club A Triples Team #1 wins 2 sets (4 points) and the match (4 points) for a total of 8 points.  Club B wins 1 set (2 points) for a total of 2 points. </p>
<p>The final score would be Club A 32 points and Club B 8 points.  Report the score in this manner. </p>
