<h3>About the Ralph Ecton Triples Tournament...</h3>

<p>The  &quot;Ralph and Marie Ecton Memorial Triples Tournament&quot; honors two people  for their years of service to the Oxnard Joslyn Lawn Bowls Club in California.</p>
<p>In November  1986, when the club began using their green, only six or seven people came out  from time to time to bowl. One of those first few bowlers was Ralph Ecton.  </p>
<p>On August 8,  1987 Ralph Ecton was elected as the first Club President. He served for two  terms, during which time he made important contributions to the growth of the  club.</p>
<p>In getting  started, the club was given a lot of help from other clubs in the Southwest  Division in the form of used equipment and training, and before long new people  started to show up. By 1987 there were 43 members. At that time, there were 36  clubs in the SWD, but Oxnard was the only club without some sort of clubhouse  adjacent to the green.</p>
<p>In 1989  Ralph went to a Recreation and Parks meeting to explain the need for a  clubhouse, and his efforts got the ball rolling toward the permanent facility  which the club now enjoys. Ralph also designed and built the lawn bowl rakes  which, slightly modified, are still in use today.</p>
<p>Ralph Ecton  died in April, 1992, and the first Ralph Ecton Memorial Triples Tournament was  held on August 18, 1993.</p>
<p>Like Ralph,  Marie Ecton was a member of the club from its inception, and served in many  ways over the years.  In 1996, she was  elected Club President, and she later served as secretary and historian for  many years, running the office like a finely tuned watch.  In 2001, when she resigned her duties for  health reasons, the Board voted to make her an Honorary Member in recognition  of her service.  In 2004, Marie passed  away, and in 2006 her name was added to the tournament which is now known as  the &quot;Ralph and Marie Ecton Memorial Triples Tournament&quot;</p>
