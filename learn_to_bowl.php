<p>What is an end? Well, baseball has &quot;innings&quot; and lawn bowls has &quot;ends.&quot; What is the jack? The &quot;jack&quot; is a small white ball that is your target. After each player's bowls are delivered on a given end, the team closest to the jack wins points for every bowl it has closer to the jack than his/her opponent. The team with the most points after all ends are played wins the match.</p>

<p>The most important equipment you would need is a set of four matched bowls. Bowls are available in different sizes (i.e., diameter), colors and the amount curve in their path when rolled. For learners, clubs typically provide the bowls as well as all other needed equipment. Once you become an active member, you are expected to acquire your own set of bowls. Used sets cost about $50 and new ones are about $400. Bowls last a very long time (decades).</p>

<p>Another piece of equipment we provide is the &quot;jack&quot; - a small white ball similar to a pool cue ball. The object of the game is to roll your bowls as close as possible to the 'jack'. The team that has a bowl nearest to the 'jack' after all bowls have been rolled scores one point. Additional points are awarded for each bowl that is closer to the jack than the nearest bowl from the other team.</p>

<p>The game of triples (three bowlers on each team) is played most commonly in this country. In triples each player delivers three bowls. On each team there are three positions: Lead (he or she will start the bowls rolling), the Vice Skip (who will play second) and the Skip (the team's captain).</p>

<p>When all the players have delivered their bowls in one direction of the green, it completes the play of one 'end'. Then the players deliver their bowls from the opposite direction of the green, completing another 'end'. The game consists of a number of 'ends' (typically 14, 16 or 18 ends in an afternoon) depending on players and their Skips.</p>

<p>One of the unique features of lawn bowling is that the bowls follow a curved path as they roll on the green. They curve because of the way they are shaped dring manufacture (this feature is referred to as 'bias'). An elegantly curving bowl is one of the most exciting and satisfying sights for an enthusiastic lawn bowler.</p>

<h3>A Bit of History</h3>

<p>The game has its roots in old England, where it has been played since the thirteenth century. One of the most famous stories indicating the devotion bowlers have to their game is related to Sir Francis Drake and the Spanish Armada. On July 18th, 1588, Drake was involved in a game of bowls at Plymouth Hoe when he was notified that the Spanish Armada was approaching. His immortalized response was &quot;We still have time to finish the match!&quot; - and thereafter the English Navy soundly defeated the Armada.</p>

