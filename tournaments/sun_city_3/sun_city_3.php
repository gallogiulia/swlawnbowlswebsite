<h3>About the Sun City Triples Tournament...</h3>

<p>The club's First Annual Invitational Tournament was played on Nov. 7, 1970, with 26 teams representing nine clubs, and has been an annual late October-early November event since that time. Several of the clubs from the first tournament are no longer active, including Arroyo Seco, the "A" green winner. The 1970 event marked the first major use of our second green, built entirely by our club members. For many years the tournament was a mixed event but more recently has been mixed or match.</p>
