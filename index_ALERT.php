<h3><span class="background_yellow">&nbsp;Bowls USA Newsletter&nbsp;</span></h3>
<p>Hello Bowlers!</p>
<p>I&rsquo;d like to tell you about the changes in the Bowls USA Newsletter and  introduce you to your new Division Editor.</p>
<p> First, the Newsletter will be published bi-monthly (every-other-month)  starting the end of March.</p>
<p>Second, you will now send all your newsletter submissions to your new  Division Editor. Unless they tell you differently, your &ldquo;drop-dead&rdquo; deadline  will be the 20th of the second month. So, your deadline for the  March newsletter will be March 20. Moving forward, your deadline for the next  newsletter will be May 20 and so on. Anything submitted later than the  &ldquo;drop-dead&rdquo; deadline will not be published until the next newsletter, no  exceptions.</p>
<p>Monthly Bowls USA column contributors will still send submissions to me  at the regular e-mail address.</p>
<p>Your Southwest Division Editor is Ronald Neef. Please send all your  submissions to him at this e-mail address:</p>
<p><a href="mailto:rrnbowlsusa@gmail.com" target="_blank">rrnbowlsusa@gmail.com</a></p>
<p>Please send all submissions as Word Docs and all photos as jpg.</p>
<p>Thank you,<br>
 Cheri Cabot<br>
 Editor-in-Chief, Bowls USA Newsletter</p>
