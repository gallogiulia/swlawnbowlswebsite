<h1 align="center" class="terms">GENERAL LAWN BOWLING TERMS</h1>
<table class="dicttable">
 <tr>
  <th>AHDU</th>
  <td><strong>A</strong>ssistant <strong>H</strong>ead <strong>D</strong>ivision <strong>U</strong>mpire</td>
 </tr>
 <tr>
  <th>ALBA</th>
  <td><strong>A</strong>merican <strong>L</strong>awn <strong>B</strong>owls <strong>A</strong>ssociation - the U.S. men's-only lawn bowls organization that was formed in 1915. ALBA and AWLBA merged on January 1, 2001 to become the USLBA.</td>
 </tr>
 <tr>
  <th>Australian Pairs</th>
  <td>A Pairs game variation with no official Lead or Skip. The players alternate bowling first and the game involves walking to the opposite side of the Rink twice during each End.</td>
 </tr>
 <tr>
  <th>AWLBA</th>
  <td><strong>A</strong>merican <strong>W</strong>omen's <strong>L</strong>awn <strong>B</strong>owls <strong>A</strong>ssociation - the U.S. women's-only lawn bowls organization that was formed in 1970. AWLBA and ALBA merged on January 1, 2001 to become the USLBA.</td>
 </tr>
 <tr>
  <th>Back Bowl</th>
  <td>A bowl that rests beyond the    Jack. </td>
 </tr>
 <tr>
  <th>Backhand</th>
  <td>For left-handers, Delivering a bowl to the right of the target with the intention that it travels in a left arc. For right-handers, Delivering a bowl to the left of the target with the intention that it travels in a right arc.</td>
 </tr>
 <tr>
  <th>Bank</th>
  <td>The vertical surface that defines the outside edge of the Ditch.</td>
 </tr>
 <tr>
  <th>Be Up</th>
  <td>Instruction from Skip to ensure that the bowl does not come to rest in front of the Jack. </td>
 </tr>
 <tr>
  <th>Bias</th>
  <td>The tendency of a bowl to travel in a curved path. The amount of curve is established during manufacture by removing material on one side of its rolling surface. The direction of curve is indicated by a small emblem on one side of the bowl (the opposite side has a larger emblem).</td>
 </tr>
 <tr>
  <th>Blocker</th>
  <td>A bowl that impedes the path to a target. </td>
 </tr>
 <tr>
  <th>Bowls USA</th>
  <td>The &quot;Doing Business As&quot; name of the U.S. bowls organization (see USLBA).</td>
 </tr>
 <tr>
  <th>Burned End</th>
  <td>(see Dead End)</td>
 </tr>
 <tr>
  <th>Catcher</th>
  <td>(See Back Bowl)</td>
 </tr>
 <tr>
  <th>CMSB</th>
  <td><strong>C</strong>onfédération <strong>M</strong>ondiale des <strong>S</strong>ports de <strong>B</strong>oules - the umbrella  International Federation for bowls type sports recognized by the International  Olympic Committee. World Bowls is a member of CMSB.</td>
 </tr>
 <tr>
  <th>Dead Bowl</th>
  <td>A bowl that either goes in the Ditch or rests completely outside the Rink. (See &quot;Toucher&quot; for an exception to this general statement)</td>
 </tr>
 <tr>
  <th>Dead End</th>
  <td>An End in which the Jack is displaced beyond the side limits of the Rink. The End is replayed.</td>
 </tr>
 <tr>
  <th>Deliver(ing)</th>
  <td>The action of rolling the bowl.</td>
 </tr>
 <tr>
  <th>Ditch</th>
  <td>The recessed area surrounding the Green. The Front Ditch is near the Head, the Back Ditch is behind the bowler.</td>
 </tr>
 <tr>
  <th>Down</th>
  <td>An indication that a team does not have the bowl closest to the Jack (the Shot Bowl). (opposite of Up)</td>
 </tr>
 <tr>
  <th>Draw Shot</th>
  <td>A bowl Delivered to a specific location without excessively disturbing bowls already in the Head.</td>
 </tr>
 <tr>
  <th>Drive</th>
  <td>Delivering a bowl with considerable force with the intention of moving    either the Jack or a specific bowl(s).</td>
 </tr>
 <tr>
  <th>End</th>
  <td>The basic element of a game of bowls (comparable to a baseball inning). It includes placing the Mat, rolling &amp; centering the Jack and Delivering all bowls of both opponents in one direction.</td>
 </tr>
 <tr>
  <th>Foot Fault</th>
  <td>A rule violation that occurs when at least one of the bowler's feet does not remain on or over the Mat    on release of the bowl.</td>
 </tr>
 <tr>
  <th>Forehand</th>
  <td>For right-handers, Delivering a bowl to the right of the target with the intention that it travels in a left arc. For left-handers, Delivering a bowl to the left of the target with the intention that it travels in a right arc.</td>
 </tr>
 <tr>
  <th>Fours (or Rinks)</th>
  <td>A bowling game in which 4 players per team compete: a Skip, a Vice, a 2nd    Lead and a Lead. Typically each player uses 2 bowls in this game.</td>
 </tr>
 <tr>
  <th>Grass</th>
  <td>Apart from the surface, a description of the angle between a straight line to the target and the direction the bowl is Delivered. If the angle is too great, the bowl will come to rest on that side of the target (&quot;Too much grass&quot;).</td>
 </tr>
 <tr>
  <th>Green</th>
  <td>The approximately 120' x 120' surface on which several games can be played simultaneously.</td>
 </tr>
 <tr>
  <th>Hammer</th>
  <td>The final bowl of the End.</td>
 </tr>
 <tr>
  <th>Hammer Rule</th>
  <td>A former law of the sport that was revoked with the most recent revision (Crystal Mark Second Edition). The law gave the team that won an End the choice of bowling first or second on the following End. The current law prescribes that the winner of an End bowls first.</td>
 </tr>
 <tr>
  <th>Hand</th>
  <td>The side of the direct path to the target on which the bowl is Delivered (see &quot;Forehand&quot; and &quot;Backhand&quot;)</td>
 </tr>
 <tr>
  <th>HDU</th>
  <td><strong>H</strong>ead <strong>D</strong>ivision <strong>U</strong>mpire</td>
 </tr>
 <tr>
  <th>Head</th>
  <td>The grouping of the Jack and bowls that develops during an End.</td>
 </tr>
 <tr>
  <th>Hog Line</th>
  <td>An imaginary line indicated by special marks (often flags for tournaments) that show the minimum    distance the Jack must be rolled when the front of the Mat is 2 meters from the Ditch.</td>
 </tr>
 <tr>
  <th>Holding Shot</th>
  <td>A phrase that describes the team with a bowl closest to Jack (see &quot;Shot Bowl&quot;). </td>
 </tr>
 <tr>
  <th>Hook</th>
  <td>The path traveled by a bowl that has an extreme amount of Bias.</td>
 </tr>
 <tr>
  <th>IBB</th>
  <td><strong>I</strong>nternational <strong>B</strong>owling <strong>B</strong>oard</td>
 </tr>
 <tr>
  <th>IWBB</th>
  <td><strong>I</strong>nternational <strong>W</strong>omen's<strong> B</strong>owling <strong>B</strong>oard - the former international women's bowls organization. It merged with the men's World Bowls Board (WBB) on July 1, 2001 to form World Bowls (WB).</td>
 </tr>
 <tr>
  <th>Jack</th>
  <td>A solid white or yellow ball approximately 64mm (2.5&quot;) in diameter that is the bowling target. Full specifications are contained in Laws of the Sport of Bowls
   Crystal Mark Second Edition, Section&nbsp;3.</td>
 </tr>
 <tr>
  <th>Lane</th>
  <td>(see &quot;Rink&quot;)</td>
 </tr>
 <tr>
  <th>Lead</th>
  <td>The team member who bowls first on each End.</td>
 </tr>
 <tr>
  <th>Line Bowl &amp;<br />
   Line Jack</th>
  <td>A  bowl or Jack that rests partly inside and partly
   outside the side boundary of the Rink. Such bowls and jacks are still in play.</td>
 </tr>
 <tr>
  <th>Markers</th>
  <td>People who assist in singles games. In pairs, triples and fours games, a team member is at the Head to center the Jack and provide information to the bowler. Markers provide these services in a singles game.</td>
 </tr>
 <tr>
  <th>Mat</th>
  <td>A 600mm x 360mm (23,6&quot; x 14.2&quot;) thin surface (similar to a door mat) that establishes the area where a bowler must stand when Delivering a bowl. The Mat is positioned by the Lead who sets the Jack at the beginning of each End. The front of the Mat must be at least 2 meters from the ditch.</td>
 </tr>
 <tr>
  <th>Measure</th>
  <td>The act of determining which team's bowl is closest to the Jack by use of a tool when a visual inspection is insufficient. The term also describes the tool itself.</td>
 </tr>
 <tr>
  <th>Member National Authority</th>
  <td>The organization that represents a particular nation in  World Bowls, the controlling organization of the sport. The USLBA is the U.S. Member National Authority.</td>
 </tr>
 <tr>
  <th>MNA</th>
  <td><strong>M</strong>ember <strong>N</strong>ational <strong>A</strong>uthority</td>
 </tr>
 <tr>
  <th>NAC</th>
  <td><strong>N</strong>orth <strong>A</strong>merican <strong>C</strong>hallenge - a biennial U.S. vs Canada tournament</td>
 </tr>
 <tr>
  <th>NUC</th>
  <td><strong>N</strong>ational <strong>U</strong>mpires <strong>C</strong>lub</td>
 </tr>
 <tr>
  <th>Narrow</th>
  <td>A description of a bowl Delivered  at too small of an angle from a straight line to the target (&quot;Not enough Grass&quot;). The bowl comes to rest opposite the Delivered side of the target. </td>
 </tr>
 <tr>
  <th>NUIC</th>
  <td><strong>N</strong>ational <strong>U</strong>mpire <strong>I</strong>n <strong>C</strong>hief</td>
 </tr>
 <tr>
  <th>Open</th>
  <td>An event that allows participation by members and non-members of the organizing authority.</td>
 </tr>
 <tr>
  <th>Pairs</th>
  <td>A game in which each team has two players (a Skip and a    Lead).  Bowlers typically use 4 bowls each. (See Australian Pairs for a variation of this game)</td>
 </tr>
 <tr>
  <th>PBA</th>
  <td><strong>P</strong>rofessional <strong>B</strong>owls <strong>A</strong>ssociation</td>
 </tr>
 <tr>
  <th>Playdowns</th>
  <td>U.S. games that determine the people who will represent their division in the Bowls USA National Championships. The rules for these games are specified beginning in paragraph 108 of the <em>Laws of the Sport of Bowls</em> booklet (find them in the Laws link).</td>
 </tr>
 <tr>
  <th>Point</th>
  <td>The basic scoring unit in a game of bowls. One point is awarded on an End to the team with a bowl closest to the Jack. Additional points are awarded to that team for each bowl closer than the nearest opposing team's bowl.</td>
 </tr>
 <tr>
  <th>Potato Bowl</th>
  <td>A bowl that hops, skips and jumps because it is not rolling on its round surface.</td>
 </tr>
 <tr>
  <th>Practice Ends</th>
  <td>(See &quot;Trial Ends&quot;)</td>
 </tr>
 <tr>
  <th>Promoting</th>
  <td>Striking and moving a bowl, causing it to lie in a better position.</td>
 </tr>
 <tr>
  <th>Quinnell</th>
  <td>A scoring system that determines the winner of a game using a combination of  Ends won and total Points.</td>
 </tr>
 <tr>
  <th>Rink</th>
  <td>The approximately 15' x 120' area on the Green on which a game is played.</td>
 </tr>
 <tr>
  <th>Rinks (or Fours)</th>
  <td>A bowling game in which 4 players per team compete: a Skip, a Vice, a 2nd    Lead and a Lead. Typically each player uses 2 bowls in this game.</td>
 </tr>
 <tr>
  <th>Road</th>
  <td>The path a bowl follows.</td>
 </tr>
 <tr>
  <th>Round Robin</th>
  <td>A type of tournament in which each team must play each other team. Another tournament type is Elimination.</td>
 </tr>
 <tr>
  <th>Shot Bowl</th>
  <td>The bowl closest to the Jack.</td>
 </tr>
 <tr>
  <th>Singles</th>
  <td>A &quot;head-to-head&quot; game in which two players compete. Bowlers typically use 4 bowls each. Markers assist in these games.</td>
 </tr>
 <tr>
  <th>Skip/Skipper</th>
  <td>Team captain who always plays last. This person is usually the    most experienced player who also guides the strategy. </td>
 </tr>
 <tr>
  <th>Tie</th>
  <td>A situation in which bowls from opposing teams are  both touching the Jack or measurement cannot readily establish which is closer.</td>
 </tr>
 <tr>
  <th>Toucher</th>
  <td>A bowl that hits the Jack when Delivered. Such bowls are marked  for identification and remain    &quot;alive&quot; even if they enter the Ditch during the End.</td>
 </tr>
 <tr>
  <th>Trial Ends</th>
  <td>Practice Ends, usually only allowed at the start of a tournament,    in which each player rolls bowls in each direction to get a &quot;feel&quot; of the green.    Such Ends are not scored.</td>
 </tr>
 <tr>
  <th>Triples</th>
  <td>A game in which each team has 3 players - a Skip, a Vice    and a Lead. Typically each player uses 3 bowls. </td>
 </tr>
 <tr>
  <th>Up</th>
  <td>An indication that a team has the bowl closest to the Jack (the Shot Bowl). (opposite of Down)</td>
 </tr>
 <tr>
  <th>USLBA</th>
  <td><strong>U</strong>nited <strong>S</strong>tates <strong>L</strong>awn <strong>B</strong>owls <strong>A</strong>ssociation - the controlling organization for bowls in the United States. It began when  ALBA and AWLBA merged on January 1, 2001. It now operates as Bowls&nbsp;USA.</td>
 </tr>
 <tr>
  <th>Vice</th>
  <td>The team member who plays before the Skip. He/she is mutually responsible (with the opposing team's Vice) for determining the    winner of the End and counting the Points.</td>
 </tr>
 <tr>
  <th>WB</th>
  <td><strong>W</strong>orld <strong>B</strong>owls</td>
 </tr>
 <tr>
  <th>WBB</th>
  <td><strong>W</strong>orld <strong>B</strong>owls <strong>B</strong>oard - the former international men's bowls organization. It merged with the International Women's Bowling Board (IWBB) on July 1, 2001 to form World Bowls (WB).</td>
 </tr>
 <tr>
  <th>World Bowls</th>
  <td>The international organization of Member National Authorities that controls the sport of bowls. It was formed on July 1, 2001 by the merger of WBB and IWBB.</td>
 </tr>
 <tr>
  <th>Weight</th>
  <td>The amount of speed applied in delivering the bowl. &quot;Heavy&quot; means that the bowl stops beyond the intended location,    while &quot;Light&quot; means that it stops short.</td>
 </tr>
 <tr>
  <th>Whites</th>
  <td>The common term for white clothing worn during tournaments and at other events.</td>
 </tr>
 <tr>
  <th>Wick</th>
  <td>The action of a bowl striking another bowl, changing its direction and speed of travel.</td>
 </tr>
 <tr>
  <th>Wide</th>
  <td>A description of a bowl Delivered at too great of an angle from a straight line to the target (&quot;Too much Grass&quot;). The bowl comes to rest on the Delivered side of the target.</td>
 </tr>
 <tr>
  <th>Woods</th>
  <td>An old term for bowls. </td>
 </tr>
 <tr>
  <th>Yard On</th>
  <td>(see &quot;Drive&quot;)</td>
 </tr>
</table>
