<h2>Welcome to Lawn Bowling</h2>

<h3>The Game</h3>

<p>Lawn Bowls is played with asymmetrical &quot;balls&quot; called &quot;bowls&quot; that are designed to follow a curved path when rolled. The object of the game is to earn points by having your (or your team's) bowls closer to the target (a small ball called a &quot;jack&quot;) than your opponent(s). Bowls are about the size of a grapefruit and weigh about 3.5 pounds (1.6 kilograms).</p>

