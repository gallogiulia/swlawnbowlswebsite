<h3>About the Joslyn Mixed Triples... </h3>
<p>Remembering Marcellus L. Joslyn.</p>
<p>In November 2007, our club was asked by the SWD to host the prestigious Joslyn Mixed Triples tournament. During the 1950s and 1960s, Marcellus Joslyn was an outstanding bowler as well as a respected member of our club and we willingly accepted this new challenge.</p>
<p>In 1955, Mr. Joslyn personally paid for enlarging our kitchen, the patio area and for adding lockers to the Ladies Locker Room. He also donated a refrigerator and other needed equipment. In 1958, Mr. Joslyn, in partnership with the City of LA, agreed to underwrite the cost of construction of an additional green, now known as our ‘lower green’. This was a costly undertaking as our lower green used to be a duck pond.</p>
<p>In 1962, (one year before his passing, at the age of 90) Mr. Joslyn endowed the Foundation bearing his name. During the following three decades, the Joslyn Foundation provided substantial financial assistance to practically every lawn bowling club in Southern California. In 1972, our club considered enlarging our patio in place of the flower garden. With permission from the Parks Department, donations from our club members and financial assistance from the Joslyn Foundation, the patio was completed in the winter of 1972. During the 1980s, both greens became uneven, overused and needed reconditioning. The cost of this major undertaking was far beyond the club’s resources, even with some contribution promised by the Parks Department.</p>
<p>Once again, the Joslyn Foundation came to the rescueand generously provided a grant to finance (by splitting the cost with the Parks Department) the reconstruction of our lower green. Finally, before its dissolution in the late 1990s, the Foundation provided further annual grants to many lawn bowling clubs, including Holmby. Prudent investing of those grants has made it possible to keep our dues at an uneconomically low level while generously providing for everything that our fortunate club offers to our members. During its existence, the Joslyn Foundation was chaired by Mr. Remy Hudson who, during 1959 and 1960 just happened to be our 14th President. Mr. Joslyn was our Emeritus President.</p>
<p>Almost all clubs, from as south as San Diego to as north as Cambria, benefited from the helping hand of Mr. Joslyn and his Foundation. As Remy Hudson remembered: “To our knowledge, no man in America has contributed so much to lawn bowling in liberal donations, in time, in recruiting effort and in ability than Marcellus L. Joslyn”. We all owe a debt to this exceptional benefactor. We are indeed honored to host this event.</p>
<p>The first Joslyn tournament was held at Santa Monica LBC in August 1985. It was a three day event on one green. Next year, it was transferred to Long Beach LBC when more teams could compete. In 1988, Newport Harbor LBC had the honor of hosting this event. $396 cash was awarded to winners of the championship flight. Next year, it was held at Laguna Beach LBC and in 1991, the Joslyn Lake Hodges club was asked to organize this two days event.</p>
<p>We are left to ponder the changes in driving conditions whenwe consider that this San Diego area club was able to attract bowlers to drive from the other clubs 60 or 100 miles away. In 2007, then-SWD Tournament Director, Bill Kagan, read our newsletter and approached us about Holmby Park LBC taking over the hosting of this meritorious event. I did not hesitate, as we always had high regard for Mr. Joslyn as a benefactor of our sport and admiringly respected his legacy. Since 2008 our club has been hosting it and we immediately increased both the number of entries and the cash awards. Last year we had 32 teams competing for the honor and for cash awards of $2,000. This year, we are hoping to match again the maximum 32 teams from 18 clubs and will distribute over $3,000 among the six best teams on both greens.</p>
<p>An event of this magnitude requires meticulous planning, many hours of preparations and a devoted group of volunteers. We are proud of continuing the legacy of this great benefactor to our sport.. The Joslyn tournament happens to embody many of the attributes and characteristics of our club. It welcomes all, social or tournament bowlers. We are known for our warm welcome and hospitality as we showcase a high level competition in our beautiful park. During these coming weeks of preparations, we have to allocate tasks that best match the skill sets of our volunteers. I hope that this remarkable group of volunteers will be ready to help make this extraordinary event a great success.</p>
<p>Tom Seres <em class="font_12">(Holmby Happenings - September, 2016)</em></p>

