<p>(as of December 9, 2017...)</p>
<p>Golden Bias Club Members:</p>
<p>It is with regret and sadness that I notify you of Bowls USA Council approval to disband the Golden Bias Club.  Due to continued declining membership contributions, it was felt that the club could no longer provide meaningful support funds to Team USA players.</p>
<p>The account balance of $1,895.92 has been donated to USLBA Memorial Fund with request to earmark the funds to be used for Team USA Selection Camp.</p>
<p>For information on making tax-deductible contributions to USLBA Memorial Fund to support the sport of bowls in the USA, please visit:  <a href="http://www.bowlsusa.us/memorial-fund.html">http://www.bowlsusa.us/memorial-fund.html</a></p>
