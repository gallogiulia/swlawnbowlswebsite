<h3 class="align_center">Origin and Evolution of a Tournament</h3>
<p><img src="2019_pic_Amador_Martinez.png" alt="" width="200" height="262" class="fltrt_10"/>Amador Martinez joined the Oxnard Joslyn Lawn Bowls Club in 1990.  After a few years it became apparent to him that there was more demand to compete in lawn bowling tournaments than there were tournaments to pick from.  If you missed an announcement the tournament would fill up quickly and you would miss out on the opportunity to compete.  In 1996 and 1997 Stu Sistad was the President of the South West Division of ALBA, our National Lawn Bowling organization at the time.  Stu was also a member of the Oxnard Joslyn LBC and the Riverside LBC.  Amador felt that if Oxnard could host another tournament it would give Oxnard very good exposure in the lawn bowling schedule  and provide another venue for competition. Amador was successful in convincing Stu Sistad to approve another tournament here in Oxnard to be called the Oxnard Triples Tournament. This became an annual event which continued for the next eight years.</p>
<p>On May 8, 2004 the Oxnard Club Board of Directors agreed to re-name Oxnard Triples the Amador Martinez Triples Tournament.  This was done to honor Amador Martinez for all his contributions to tournament competition and his efforts as our greenskeeper in providing an excellent playing surface on the green.  We have one of the finest greens in the Division. </p>

<p class="fltrt_30">
by Bob Smith
