<h1>BOWLS USA NEWSLETTER SUBMISSIONS</h1>

<p><strong>The BowlsUSA Newsletter Editor-in-Chief, Georgie Deno, has provided details on how and when to submit articles for publication in what will now be a monthly publication.</strong></p>

<ul class="describe">

<li>Cynthia Murray is the Southwest Division Editor.  Send submissions to Cynthia at <a href="mailto:cnthmurray@gmail.com">cnthmurray@gmail.com</a>.</li>

<li>The deadline for submissions is the 15th of the month preceding publication.  For example, clubs need to submit items for the December 2018 Newsletter not later than November 15.</li>

<li>All written submissions must be Word documents.</li>

<li>Photos must be jpg files and must be clear.  (Fuzzy photos will not be used.)</li>

<li>Newsletter content will be shifting toward more general interest stories, rather than listings of local tournament results since that information is typically available from other sources such as club, division, and/or BowlsUSA websites.</li>

<li>For ideas on articles, contact Cynthia or take a look at the content of the BowlsUSA Newsletter, <a href="http://www.bowlsusa.us/uploads/7/5/9/0/75903269/december_2018_busa_newsletter.pdf">December&nbsp;2018&nbsp;edition</a>.</li>

<li>When in doubt, send the article in, but keep in mind that the Newsletter Editor-in-Chief makes the final call on what will be included and when.</li>

<li>Most important of all is to keep your stories and photos interesting.</li>

</ul>



