<p class="about">HISTORY OF THE CALIFORNIA BEARS<br>
<br>
From long before 1986, there was an event known as the California State Open. The tournament was designed to have the Pacific Inter Mountain (PIM) and South West (SW) Divisions compete against each other. The event host would be rotated between the two divisions, and the format was the same as a Division Open.
<br>
<br>
From about 1990 forward, the tournament was hosted only in the South West Division, and entries from the PIM Division began dropping. Thus, it was agreed upon by the two divisions to disband the event as it gave expense and appearance that the SW was hosting 
two Division Opens.
<br>
<br>
In 1997, Corinna Folkins, then President of the SW Women, asked Heather Stewart and Kottia Spangler to propose an alternate tournament to replace the California State Open. The two players got together and came up with a format that would involve team-play, last for one day, and allow female players from other divisions to also enter. It was important that no team played the same opponent twice, and if possible, no club played another team from their club....all this, of course, dependent on entries. This would initially be known as the California Fours. The first event was played in 1998 at Newport Harbor Lawn Bowling Club.<br>
<br>
<br>
Mary Jane Heinrich (from the Hemet Lawn Bowling Club) was enlisted to produce trophies for the event. The trophies would be perpetual, with each player of the winning team having one to display for one year. A placard would go on the trophy, until all four sides were full. Then the oldest placard would be removed and replaced with the newest winners. In 1997, bear brooch/necklaces were awarded to the winners and the founding committee. In 2018 Jerry Grady, of the Newport Harbor LBC, designed and created the wall hanging shown being held by the winners. They get to keep the wall hanging, but the bear trophy continues on as originally conceive</p>

