<h3>About the Bill Hiscock 2-Bowl Mixed Triples Tournament...</h3>

<p><img src="2016_pic_trophy_photo.png" alt="" width="150" height="319" class="trophy"/>Bill Hiscock started bowling in 1972 at the San Diego Lawn Bowling Club. He served as President of the San Diego Club and the Coronado Club. The Coronado Lawn Bowling Club started in 1935, but was terminated for several years in the late 1990s when the lawn bowling green was destroyed while being used as a staging area for construction related to remodeling the Coronado Library.</p>
<p>Bill led a successful movement of interested residents of Coronado to retain and resurrect the bowling green. His tireless efforts were accordingly recognized in 2011 with the SWD's President's Award. </p>
<p>In 2011, he captained the Coronado Club's Team to its first championship in the SWD Five Man All Star Competition. Bill is now the only USA Bowls Hall of Fame member in San Diego County.</p>
<p>In 2013, Jim Olson and Ed Quo honored Bill by establishing the Bill Hiscock Two-Bowl Mixed Triples at the Coronado Lawn Bowling Club.<br>
