<h1>HISTORY</h1>

<div class="item">

<p><a href="history/Booklet_1989.pdf" target="_blank"><img src="history/booklet_250.png" alt="Southwest Booklet 1986" width="250" height="389" class="floatRight"></a>Records show that the California Lawn Bowling Association was formed in 1921 with a membership of four clubs.<br>
</p>

<div class="blank_10"></div>

<p>Mel Hooper, a member of the Redlands Club, organized the Southern California Lawn Bowling Association in 1924 and acted in the capacity of president for three years.<br>
</p>

<div class="blank_10"></div>

<p>By 1931 the Association had 9 member clubs: Angelus, Claremont, Beverly Hills, Los Angeles, Pasadena, Pachappa, Redlands, Riverside, and Long Beach. By 1933 Highland, Park, Holmby Park, Inglewood, Laguna Beach and San Diego had joined.<br>
</p>

<div class="blank_10"></div>

<p>In 1959, with 17 affiliated clubs, the National Association was petitioned to name our Association the Southwest Division. Finally on October 1, 1967 Division status was granted.<br>
</p>

<div class="blank_10"></div>

<p>The above information and a lot more is contained in the 1989 32&#8209;page booklet pictured at right <span class="font_12"><em>(click on it to download the 11 MB PDF)</em></span>. It was prepared by Arthur&nbsp;H.&nbsp;Hansen, a member of the Arroyo&nbsp;Seco &amp; Pasadena LBCs, Southwest Division president in 1977, its secretary-treasurer 1979-85, and president of ALBA, the national organization, 1982-84.</p>

<div class="blank_10"></div>

<p>In 1990, the South Central Division was formed from the clubs east of California that were previously members of the Southwest Division.</p>
</div>


