<p><strong><u>Vision Statement:</u></strong> <br>
For the SW Division&rsquo;s club bowlers, with all levels of experience and ability, to compete against and socialize with other SW Division club bowlers.</p>
<p><strong><u>Teams: </u></strong> <br>
All clubs are encouraged to participate in the Winter League, and can enter as many teams as they can support. </p>
<p><u>A roster for every team must be provided no later than <strong>October 31, 2118</strong>.  All players that are on the roster may play any position for that team. &nbsp;Players on the roster for one team may not play for any other team.  Players who are not on the roster for a team may play as a substitute, but substitutes are not allowed to skip or play singles.</u></p>
<p>Generally, each team will have 7 bowlers on its roster.  7 bowlers are needed to support a team because of absence due to illness, injury, vacations, etc.  Generally 4 bowlers will play each week unless different bowlers bowl in the morning and afternoon.  Substituting bowlers in the afternoon is permitted.  Games can only be rescheduled because of rain. <strong><u>Games cannot be rescheduled because of player unavailability</u></strong><u>.</u>Teams need to ensure that they have sufficient back up players able to fill in as necessary.  Clubs may elect to play on Sunday as long as both clubs agree to the change. Playing on Saturday is mandatory for the playoffs. </p>
<p>Bowlers must be a member of the club that the bowler is playing for. </p>
<p>Teams may wear matching colored uniforms.  Teams not wearing matching uniforms must wear white tops and bottoms. </p>
<p>Trophies will be awarded to the teams finishing at the top in the playoffs, Gold and Silver. </p>
<p><strong><u>Structure: </u></strong><strong><u> </u></strong><br>
The competition will be in the form of regionalized leagues. The League will be sensitive to geographical and travel considerations. There will be a North and South Division with a Gold and Silver league in each Division. If possible the two leagues will meet in a playoff final but that is to be determined by the number of teams entered in each league and if the leagues stays on schedule. </p>
<p><strong><u>Gold League</u></strong> <br>
Gold League will be for more experienced and accomplished bowlers within the Division.<br>
  <br>
<strong><u>Silver League</u></strong> <br>
Silver League will be focused on learning and social aspects of the game. &nbsp;This league is composed of those bowlers who are newer to the game and would like to develop their game further.  It is hoped that the more experienced bowlers would mentor the newer bowlers. &nbsp;It is hoped that more competitive and experienced bowlers will be playing in the Gold League.  Bowlers who have finished in the top 3 in a SW points event in the last two years are not allowed to play in a Silver League game, with two exceptions. &nbsp;(1) Novices are always eligible for Silver and (2) Each Silver team may be allowed one Gold player.  The idea being that they can &ldquo;Mentor&rdquo; the other members of the team.  <u>The &ldquo;Mentor&rdquo; may not skip or play singles.  The &ldquo;Mentor&rdquo; may not leave his Silver team to substitute on another team Gold or Silver.</u>  <u> </u></p>
<p><strong><u>Format: </u></strong><u> </u><br>
4 players will play each match. 2 bowlers play pairs and the other 2 play pairs in the morning.  One Bowler plays single and the other 3 bowlers play triples in the afternoon.  There will be two games of pairs (15 ends) in the morning (Club 1 Pair A will play Club 2 Pair A and Club 1 Pair B will play Club 2 Pair B).  A game of Singles (15 ends) and Triples (15 ends) in the afternoon. </p>
<p><strong><u>Scoring: </u></strong><u> </u><br>
Each match will consist of 15 ends divided into 3 sets of 5 ends. Each set will be worth 2 skins. The team that scores the most points in that set will win the 2 skins.  Should the set end in a tie, each team will receive 1 skins.  The team that wins the match (most total points after 15 ends) will earn 4 skins.  Should the match end in a tie both teams will score 2 skins.  There will be a total of 10 skins available per match (6 set skins and 4 match skins) and a total of 40 skins is available for the day (4 games of 10 skins each). </p>
<p><strong><u>Playoffs: </u></strong><u> </u><br>
At the end of the season the winner of the North Division will play the winner of the South Division for 1st and 2nd place of the SW Division Winter League.  The 2nd place finisher of the North Division will play the 2nd place finisher of the South Division with the winner taking 3rd place of the SW Division Winter League.  This playoff format will be used for both Gold and Silver.  Wins and losses will not matter as far as 1st or 2nd place finish.  The finishers will be select based solely on skins won.  In the event of a tie, Head to Head results vs the team you are tied with will break the tie.  Should a tie still exist after considering the Head to Head results, a flip of a coin will determine the winner. </p>
<p>A tie in a playoff match, ie 20 to 20, will be decided by playing one extra triples end (the 3 bowlers who bowled triples). </p>
<p><strong><u>Entry Fee: </u></strong><u> </u><br>
The entry fee is for a team to play in the winter league is $30 per team, Gold or Silver level.  <strong><u>The $30 entry fee must be paid by October 31, 2018 for a team to be entered in the league.</u>  </strong>The $30 amount will cover administrative expenses and the purchase of trophies. <u>Checks are to be payable to: SWLBA and mailed to Lou Cohen at 1048 Irvine Ave. #371, Newport Beach, CA 92660 cell (949)466-7999.</u></p>
<p><strong><u>Match Fees: </u></strong><u> </u><br>
A $1 per player fee ($4 per team) will be collected by the host club prior to play.  </p>
<p><strong><u>Results and Administration: </u></strong><u> </u><br>
Team captains must email the results after completion of your weekly matches to Lou Cohen at  <strong><a href="mailto:loucnb@gmail.com">loucnb@gmail.com</a></strong>.  For questions or issues relating to league structure, match days and time, or player eligibility, please call or email the league chairperson (949)466-7999. </p>
<p><strong><u>Rules and Format of Play</u></strong><strong><u> </u></strong><br>
1. Games are scheduled to start at 9 AM on the scheduled date. <br>
a. Complete teams should arrive by 8:30 am. <br>
b. Players need to bring their own lunch. Hospitality is not expected from the host club. <br>
c. In the event of Rain, Captains call the opponent&rsquo;s captain no later then Friday to confirm the match is on. <br>
d. If a full team cannot be fielded, the captain should call the opposing captain and the league chairman. If a match is <br>
    forfeited the winning team will get 40 skins awarded and the forfeiting team will get 0 skins. <br>
e. If a match is rained out, please re-schedule a makeup date with the opposing captain and let the league chairman<br>
    know about the change. If possible, make up the match as quickly as possible so the league can remain on schedule. <br>
f. In all cases, call the host club if weather is questionable. It may be raining in your area and sunny at your host club<br>
   or vice versa. </p>
<p>2. The 1 minute mat rule will apply to the Gold League.  Only Skips can visit the head and only after the 2nd bowl is delivered </p>
<p>3. There will be no dead ends.  The jack will be re-spotted at the 2 meter T.  </p>
<p>4. In the event of any unforeseen circumstances the Winter League Chairperson will have final ruling as to any modifications of start times, format changes or rule interpretations. </p>
<p>5. &nbsp;<u>A team that arrives late will be penalized one (1) point for each ten (10) minutes from the end of trial ends, or fraction thereof, and each point will count as an end played.  A team that is thirty (30) minutes late will forfeit, score 0 skins, and that team&rsquo;s opponent will score 40 skins.  Such a penalty may be waived by the Tournament Director due to extenuating circumstances.</u><u> </u></p>
