<h1><img src="tournaments/swlba_waterbury/0_trophy.png" alt="Waterbury Trophy" width="150" height="198" class="trophy" title="Waterbury Trophy">WATERBURY</h1>

<p>The &quot;Waterbury All-League Championship&quot; is a competition among clubs that represent five SWLBA leagues (<a href="documents/SWLBA_Blue_Book.pdf#water">find details here</a>). For this competition, clubs are organized as shown in the chart below. The leagues have the same names as five SWLBA districts, but some clubs in unrepresented districts play with other leagues.</p>

<p>The winning club takes possession of the trophy (shown at right) on which the names of winning clubs are engraved. The history of winners and the number of times each club won are provided below <a href="#prior">(prior winners</a> - <a href="#times">times clubs won</a>).</p>

<p>Carl Waterbury provided the initial financial support and a perpetual trophy in 1983.</p>
<p>San Diego won the 2018 competition November 17th at Long Beach (see chart below).<br>
</p>



