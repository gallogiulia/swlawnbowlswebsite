<h3>About the Don Goodrich Senior Triples Tournament...</h3>

<p>Since our club is part of a senior community with membership limited to persons 55 or older, we decided more than 10 years ago that it would be appropriate to host a tournament for seniors.  The first event was in 2004 and limited to bowlers 65 or older, but now has an age 55 limit.  Don Goodrich, a fine bowler, was club president in 2004 and had previously held various offices, and had won three Men's Singles titles and two Mixed Doubles. The tournament was renamed in Don's honor after his death in 2006. </p>
<p>In 2018 the event was changed to Australian Pairs.</p>
